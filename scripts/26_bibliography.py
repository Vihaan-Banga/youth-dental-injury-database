def escape_bib(s):
    """Escape BibTeX special chars in a string."""
    return (s or "").replace("&", r"\&").replace("%", r"\%").replace("_", r"\_").replace("#", r"\#")


def parse_authors(rec):
    """Return BibTeX 'Last, FM and Last, FM and ...' format."""
    authors = rec.get("authors") or []
    parts = []
    for a in authors:
        a = a.strip()
        if not a: continue
        # esummary format: "LastName Initials"
        m = re.match(r"^(\S+(?:\s\S+)*?)\s+([A-Z][A-Z]?)$", a)
        if m:
            parts.append(f"{m.group(1)}, {m.group(2)}")
        else:
            parts.append(a)
    return " and ".join(parts) if parts else "Unknown"


def get_doi(pmid):
    """Pull DOI from esummary JSON."""
    summary = ROOT / "data/raw/papers/_search_logs/pubmed_esummary_2026-05-21.json"
    if not summary.exists():
        return ""
    try:
        data = json.load(open(summary))
        rec = data.get("result", {}).get(pmid, {})
        for art in rec.get("articleids", []):
            if art.get("idtype") == "doi":
                return art.get("value", "")
    except (json.JSONDecodeError, KeyError):
        pass
    return ""


