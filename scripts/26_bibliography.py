    if not f.exists():
        skipped += 1
        continue
    rec = json.load(open(f))
    key = PMID_TO_SOURCE_ID.get(pmid, f"PMID-{pmid}")
    title = escape_bib(rec.get("title", "Untitled"))
    authors = escape_bib(parse_authors(rec))
    journal = escape_bib(rec.get("journal", ""))
    year = rec.get("pub_year", "")
    doi = get_doi(pmid)

    e = [f"@article{{{key},",
         f"  title   = {{{title}}},",
         f"  author  = {{{authors}}},",
         f"  journal = {{{journal}}},",
         f"  year    = {{{year}}},",
         f"  pmid    = {{{pmid}}},"]
    if doi:
        e.append(f"  doi     = {{{doi}}},")
        e.append(f"  url     = {{https://doi.org/{doi}}},")
    else:
        e.append(f"  url     = {{https://pubmed.ncbi.nlm.nih.gov/{pmid}/}},")
    e.append("}\n")
    entries.append("\n".join(e))

